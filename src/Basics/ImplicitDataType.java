package Basics;

//	/smaller or same data type value is transfered to bigger or same size datatype

public class ImplicitDataType {

	public static void main(String[] args) {

		byte b = 8; // 1 byte
		short s; // 2 byte
		int i = 10; // 4 byte
		long l; // 8 byte
		
		i = b; //ImplicitDataType : happen automatically

	}

}
